#include <iostream>
using namespace std;
#include <vector>
/*
	vector容器中存放自定义数据类型

*/
class Person {
public:
	Person(string name, int age) {
		this->m_Name = name;
		this->m_Age = age;
	}

	string m_Name;
	int m_Age;
};
void test02() {
	vector<Person>v;
	Person p1("张三", 12);
	Person p2("李四", 22);
	Person p3("王五", 32);
	Person p4("赵柳", 42);

	//向容器中添加数据
	v.push_back(p1);
	v.push_back(p2);
	v.push_back(p3);
	v.push_back(p4);

	//遍历元素中的数据
	for (vector<Person>::iterator it = v.begin(); it != v.end(); it++) {
		cout << "姓名：" << (*it).m_Name << "  ";
		cout << "年龄：" << it->m_Age	<< endl;
	}
}

//存放自定义类型的指针
void test02_1() {
	vector<Person*>v;
	Person p1("张三", 12);
	Person p2("李四", 22);
	Person p3("王五", 32);
	Person p4("赵柳", 42);

	//向容器中添加数据
	v.push_back(&p1);
	v.push_back(&p2);
	v.push_back(&p3);
	v.push_back(&p4);

	//遍历容器
	for (vector<Person*>::iterator it = v.begin(); it != v.end(); it++)
	{
		cout << "姓名：" << (*it)->m_Name << "  ";
		cout << "年龄：" << (*it)->m_Age << endl;
	}
}
